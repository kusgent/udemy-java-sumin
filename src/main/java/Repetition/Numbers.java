package Repetition;

public class Numbers {
    public static void main(String[] args) {
        int a;
        int b;
        int c;
        a = 10;
        b = 3;
        c = a / b;
        System.out.println("c = " + c);
        c = a % b;
        System.out.println("c = " + c);
        c += 5;
        System.out.println(c);
        c -= 1;
        System.out.println(c);
        c *= 2;
        System.out.println(c);
        c /= 5;
        System.out.println(c);
    }
}
